// Local and Global Variable 

#include <iostream>
using namespace std;
int a =29; // Global variable
int main()
{
	int b = 3;// Local variable 
	
	cout<< "value of a:" << a << endl;
	cout<< "value of b:" << b << endl;
	return 0;
}
//Global variable - a variable which can be used inside or outside the main function.
//Local variable - a variable which can only used inside the function.
